package helpers

func NormalizeNumber(e rune) int {
	switch e {
	case 1632:
		return 0
	case 1633:
		return 1
	case 1634:
		return 2
	case 1635:
		return 3
	case 1636:
		return 4
	case 1637:
		return 5
	case 1638:
		return 6
	case 1639:
		return 7
	case 1640:
		return 8
	case 1641:
		return 9
	}
	return 0
}
